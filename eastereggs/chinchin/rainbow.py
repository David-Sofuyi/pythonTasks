import random

random_num = random.randint(1, 7)  # 1 to 7 inclusive

if random_num == 1:
    print(f"{random_num} is violet\n")
elif random_num == 2:
    print(f"{random_num} is indigo\n")
elif random_num == 3:
    print(f"{random_num} is blue\n")
elif random_num == 4:
    print(f"{random_num} is green\n")
elif random_num == 5:
    print(f"{random_num} is yellow\n")
elif random_num == 6:
    print(f"{random_num} is orange\n")
elif random_num == 7:
    print(f"{random_num} is red\n")
